class Empleado:
    def __init__(self, id_empleado, nombre, direccion, telefono, email, fecha_contrato, salario):
        self.id_empleado = id_empleado
        self.nombre = nombre
        self.direccion = direccion
        self.telefono = telefono
        self.email = email
        self.fecha_contrato = fecha_contrato
        self.salario = salario
